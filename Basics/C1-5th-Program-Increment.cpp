
#include<iostream>
using namespace std;
int main()
{
    int w = 3;
    cout<<w<<endl;
    cout<<w++<<endl;
    cout<<w<<endl<<endl;

    // w++ is called post increment operator.

    int x = 4;
    cout<<x<<endl;
    cout<<++x<<endl;
    cout<<x<<endl<<endl;

    // ++x is called pre increment operator.

    return 0;
}
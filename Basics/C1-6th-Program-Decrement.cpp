
#include<iostream>
using namespace std;
int main()
{
    int y = 5;
    
    cout<<y<<endl;
    cout<<y--<<endl;
    cout<<y<<endl<<endl;

    // y-- is called post decrement operator.

    int z = 6;
    cout<<z<<endl;
    cout<<--z<<endl;
    cout<<z<<endl<<endl;

    // --z is called pre decrement operator.

    return 0;
}